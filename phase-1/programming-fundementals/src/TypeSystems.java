public class TypeSystems
{
    public static void main(String[] args)
    {
        System.out.println(true);
        System.out.println(false);

        System.out.println(010);
        System.out.println(7);

        System.out.println(0x13);
        System.out.println(0xe);
        System.out.println(0xa);
        System.out.println(0x9);
        System.out.println(0x2);


        System.out.println(4.82);
        System.out.println(12.768);
        System.out.println(010.768);   //floating point numbers only can represtent as decimal
        System.out.println(10.12E+7);   //scientific notation


        System.out.println('a'); //char representation
        System.out.println("This is a String"); //string
        System.out.println(0x1p-3);

        System.out.println(1000154894547898988l);





    }
}
